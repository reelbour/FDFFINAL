#include "libft.h"

void	ft_putstr(char const *s)
{
	size_t	i;
	char	*ps;

	if (s == NULL)
		return ;
	ps = (char *)s;
	i = 0;
	while (ps[i] != '\0')
	{
		ft_putchar((char)ps[i]);
		i++;
	}
}
