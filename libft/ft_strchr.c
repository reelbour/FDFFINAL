#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*ps;
	size_t	i;

	i = 0;
	ps = (char *)s;
	while (ps[i] != c)
	{
		if (ps[i] == '\0')
			return (NULL);
		i++;
	}
	return (ps + i);
}
